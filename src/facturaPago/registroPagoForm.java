    private void txtValorPagoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtValorPagoFocusLost
        String valor = txtValorPago.getText();
        if(!esNumero(valor))
        mensaje = "El valor del pago ingresado no es un número";
    }//GEN-LAST:event_txtValorPagoFocusLost

    private void txtValorPagoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorPagoKeyReleased
        String valor = txtValorPago.getText();
        if(!esNumero(valor))
        txtValorPago.setText("");
        try
        {
            int valorInt = Integer.parseInt(valor);
            if(valorInt<0)
            txtValorPago.setText("");
        }catch(Exception e)
        {

        }
    }//GEN-LAST:event_txtValorPagoKeyReleased

    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed
        String valor = txtValorPago.getText();
        String NoReceta = lblIdReceta.getText();
        String codCita = lblCodigoCita.getText();
        if(valor.equals("") || NoReceta.equals("") || codCita.equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Se encuentra vacío alguno de los campos, no se puede generar la receta.");
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Registro Exitoso.");
        }
    }//GEN-LAST:event_btnRegistroActionPerformed

