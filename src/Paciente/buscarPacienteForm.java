    private void txtAñoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtAñoFocusLost
        String anio = txtAño.getText();
        int numanio = 0;
        Calendar actual = Calendar.getInstance();
        int anioActual = actual.get(Calendar.YEAR);
        MensajeBuscarPaciente8 = "";

        if (anio.equals(""))
        MensajeBuscarPaciente8 += "El año no puede estar vacío\n";

        if(!esNumero(anio))
        MensajeBuscarPaciente8 += "El año no puede contener espacios ni caracteres alfabéticos";
        else{
            if(!anio.equals("")){
                numanio = Integer.parseInt(anio);
                if(numanio < 1914 || numanio > anioActual)
                MensajeBuscarPaciente8 += "El año supera al año actual o es menor que el año mínimo aceptado(1914)\n";
            }
        }
    }//GEN-LAST:event_txtAñoFocusLost

